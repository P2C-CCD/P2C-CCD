void WriteHtmlSuffix(std::ostream& out) {
  out << R"P2HTML(
};

const canvas = document.getElementById('view');
const ctx = canvas.getContext('2d');
const patchColors = ['#d79a4a', '#70b7a8', '#c88bd8', '#8ba5d8'];

function resize() {
  const dpr = window.devicePixelRatio || 1;
  canvas.width = Math.floor(canvas.clientWidth * dpr);
  canvas.height = Math.floor(canvas.clientHeight * dpr);
  ctx.setTransform(dpr, 0, 0, dpr, 0, 0);
  draw();
}

function eachPoint(callback) {
  for (const tri of scene.triangles) for (const v of tri.verts) callback(v);
  for (const box of scene.aabbs) { callback(box.min); callback(box.max); }
  for (const cap of scene.capsules) { callback(cap.p0); callback(cap.p1); }
  for (const path of scene.paths) for (const p of path.points) callback(p);
}

function bounds() {
  let minX = Infinity, minY = Infinity, maxX = -Infinity, maxY = -Infinity;
  eachPoint((p) => {
    minX = Math.min(minX, p[0]); maxX = Math.max(maxX, p[0]);
    minY = Math.min(minY, p[1]); maxY = Math.max(maxY, p[1]);
  });
  return {minX, minY, maxX, maxY};
}

function makeProjector() {
  const b = bounds();
  const width = canvas.clientWidth;
  const height = canvas.clientHeight;
  const pad = 70;
  const sx = (width - pad * 2) / Math.max(1e-9, b.maxX - b.minX);
  const sy = (height - pad * 2) / Math.max(1e-9, b.maxY - b.minY);
  const scale = Math.min(sx, sy);
  const ox = 0.5 * (width - scale * (b.maxX + b.minX));
  const oy = 0.5 * (height + scale * (b.maxY + b.minY));
  return {
    p: (v) => [ox + v[0] * scale, oy - v[1] * scale],
    r: (value) => Math.max(1.0, value * scale),
  };
}

function drawGrid(project) {
  ctx.save();
  ctx.strokeStyle = 'rgba(255,255,255,0.06)';
  ctx.lineWidth = 1;
  for (let x = -2; x <= 6; x += 0.5) {
    const a = project.p([x, -2, 0]);
    const b = project.p([x, 5, 0]);
    ctx.beginPath(); ctx.moveTo(a[0], a[1]); ctx.lineTo(b[0], b[1]); ctx.stroke();
  }
  for (let y = -2; y <= 5; y += 0.5) {
    const a = project.p([-2, y, 0]);
    const b = project.p([6, y, 0]);
    ctx.beginPath(); ctx.moveTo(a[0], a[1]); ctx.lineTo(b[0], b[1]); ctx.stroke();
  }
  ctx.restore();
}

function polygon(points, fill, stroke, alpha, lineWidth) {
  ctx.save();
  ctx.globalAlpha = alpha;
  ctx.beginPath();
  ctx.moveTo(points[0][0], points[0][1]);
  for (let i = 1; i < points.length; ++i) ctx.lineTo(points[i][0], points[i][1]);
  ctx.closePath();
  if (fill) { ctx.fillStyle = fill; ctx.fill(); }
  if (stroke) { ctx.strokeStyle = stroke; ctx.lineWidth = lineWidth; ctx.stroke(); }
  ctx.restore();
}

function draw() {
  ctx.clearRect(0, 0, canvas.clientWidth, canvas.clientHeight);
  const project = makeProjector();
  drawGrid(project);

  for (const box of scene.aabbs) {
    const min = project.p(box.min);
    const max = project.p(box.max);
    const x = Math.min(min[0], max[0]);
    const y = Math.min(min[1], max[1]);
    const w = Math.abs(max[0] - min[0]);
    const h = Math.abs(max[1] - min[1]);
    ctx.save();
    ctx.fillStyle = `hsla(${box.hue}, 75%, 62%, 0.075)`;
    ctx.strokeStyle = `hsla(${box.hue}, 90%, 72%, 0.36)`;
    ctx.lineWidth = 1.5;
    ctx.fillRect(x, y, w, h);
    ctx.strokeRect(x, y, w, h);
    ctx.restore();
  }

  for (const cap of scene.capsules) {
    const a = project.p(cap.p0);
    const b = project.p(cap.p1);
    ctx.save();
    ctx.strokeStyle = 'rgba(233,224,166,0.28)';
    ctx.lineWidth = project.r(cap.r) * 2.0;
    ctx.lineCap = 'round';
    ctx.beginPath(); ctx.moveTo(a[0], a[1]); ctx.lineTo(b[0], b[1]); ctx.stroke();
    ctx.strokeStyle = 'rgba(233,224,166,0.86)';
    ctx.lineWidth = 2;
    ctx.beginPath(); ctx.moveTo(a[0], a[1]); ctx.lineTo(b[0], b[1]); ctx.stroke();
    ctx.restore();
  }

  for (const path of scene.paths) {
    ctx.save();
    ctx.strokeStyle = path.color;
    ctx.lineWidth = 2;
    ctx.setLineDash([4, 5]);
    ctx.beginPath();
    path.points.forEach((p, i) => {
      const q = project.p(p);
      if (i === 0) ctx.moveTo(q[0], q[1]); else ctx.lineTo(q[0], q[1]);
    });
    ctx.stroke();
    ctx.setLineDash([]);
    for (const p of path.points) {
      const q = project.p(p);
      ctx.beginPath(); ctx.arc(q[0], q[1], 2.4, 0, Math.PI * 2); ctx.fillStyle = path.color; ctx.fill();
    }
    ctx.restore();
  }

  for (const tri of scene.triangles) {
    const pts = tri.verts.map(project.p);
    const color = patchColors[tri.patch % patchColors.length];
    if (tri.layer === 't0') {
      polygon(pts, color, 'rgba(0,0,0,0.45)', 0.72, 1.0);
    } else {
      polygon(pts, null, color, 0.85, 2.2);
    }
  }
}

window.addEventListener('resize', resize);
resize();
</script>
</body>
</html>
)P2HTML";
}


int main(int argc, char** argv) {
  const std::filesystem::path output_path =
      argc > 1 ? std::filesystem::path(argv[1])
               : std::filesystem::path("src/outputs/geometry_proxy_debug.html");

  p2cccd::Mesh mesh = MakeDebugMesh();
  std::vector<p2cccd::Patch> patches;
  if (!CheckOk(p2cccd::BuildPatchesFromMeshPatchIds(mesh, &patches), "BuildPatchesFromMeshPatchIds")) {
    return 1;
  }

  const p2cccd::MotionSegment motion = MakeDebugMotion();
  std::vector<p2cccd::TimeSlab> slabs;
  if (!CheckOk(p2cccd::GenerateTimeSlabsForMotionSegment(motion, 4, &slabs),
               "GenerateTimeSlabsForMotionSegment")) {
    return 1;
  }

  p2cccd::PoseSample pose_t0;
  p2cccd::PoseSample pose_t1;
  if (!CheckOk(p2cccd::InterpolateRigidMotion(motion, motion.t0, &pose_t0),
               "InterpolateRigidMotion(t0)") ||
      !CheckOk(p2cccd::InterpolateRigidMotion(motion, motion.t1, &pose_t1),
               "InterpolateRigidMotion(t1)")) {
    return 1;
  }

  std::filesystem::create_directories(output_path.parent_path());
  std::ofstream out(output_path);
  if (!out) {
    std::cerr << "failed to open output file: " << output_path.string() << '\n';
    return 1;
  }

  WriteHtmlPrefix(out);

  out << "triangles:[\n";
  if (!WriteTransformedMesh(out, mesh, pose_t0, "t0", true)) {
    return 1;
  }
  if (!WriteTransformedMesh(out, mesh, pose_t1, "t1", false)) {
    return 1;
  }
  out << "\n],\n";

  out << "aabbs:[\n";
  bool first = true;
  const double eps_proxy = 0.04;
  for (const p2cccd::Patch& patch : patches) {
    for (const p2cccd::TimeSlab& slab : slabs) {
      p2cccd::EndpointSweptAabbProxy proxy;
      if (!CheckOk(p2cccd::BuildEndpointSweptAabbProxy(patch, motion, slab, eps_proxy, &proxy),
                   "BuildEndpointSweptAabbProxy")) {
        return 1;
      }
      if (!first) {
        out << ",\n";
      }
      first = false;
      out << "{patch:" << patch.patch_id << ",slab:" << slab.slab_id << ",hue:"
          << (38 + 105 * patch.patch_id + 15 * slab.slab_id) << ",min:";
      WriteVec3(out, proxy.bounds.min);
      out << ",max:";
      WriteVec3(out, proxy.bounds.max);
      out << '}';
    }
  }
  out << "\n],\n";

  out << "capsules:[\n";
  first = true;
  for (const p2cccd::Patch& patch : patches) {
    for (const p2cccd::TimeSlab& slab : slabs) {
      p2cccd::CapsuleProxy proxy;
      if (!CheckOk(p2cccd::BuildCapsuleProxy(patch, motion, slab, eps_proxy, &proxy),
                   "BuildCapsuleProxy")) {
        return 1;
      }
      if (!first) {
        out << ",\n";
      }
      first = false;
      out << "{patch:" << patch.patch_id << ",slab:" << slab.slab_id << ",p0:";
      WriteVec3(out, proxy.endpoint0);
      out << ",p1:";
      WriteVec3(out, proxy.endpoint1);
      out << ",r:" << proxy.radius << '}';
    }
  }
  out << "\n],\n";

  out << "paths:[\n";
  first = true;
  for (const p2cccd::Patch& patch : patches) {
    if (!first) {
      out << ",\n";
    }
    first = false;
    out << "{patch:" << patch.patch_id << ",color:'"
        << (patch.patch_id == 0 ? "#ffffff" : "#bbfff1") << "',points:[";
    for (std::uint32_t i = 0; i <= 24; ++i) {
      const double t = static_cast<double>(i) / 24.0;
      p2cccd::PoseSample pose;
      Vec3 center{};
      if (!CheckOk(p2cccd::InterpolateRigidMotion(motion, t, &pose), "InterpolateRigidMotion") ||
          !CheckOk(p2cccd::TransformPoint(pose, patch.local_center, &center), "TransformPoint")) {
        return 1;
      }
      if (i != 0) {
        out << ',';
      }
      WriteVec3(out, center);
    }
    out << "]}";
  }
  out << "\n]\n";

  WriteHtmlSuffix(out);
  std::cout << "wrote " << std::filesystem::absolute(output_path).string() << '\n';
  return 0;
}
